class deviation
{
	public:
		int P = 70;
		int I = 0;
		int D = 5;
		int distance = 0;
		int distanceMem[40] = { 0 };
		int distanceSum = 0;

		void pushIntoDistanceMem(int element)
		{
			for (int i = 39; i > 0; i--)
			{
				distanceMem[i] = distanceMem[i - 1];
			}
			distanceMem[0] = element;
		}
		void getDistanceMemSum()
		{
			for (int i = 0; i < 40; i++)
			{
				distanceSum += distanceMem[i];
			}
		}
};

